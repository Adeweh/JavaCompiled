public class Number {
	public static void main(String [] args) {
		int d = 1;
		int o = 2;
		int r = 3;
		int c = 4;

	System.out.println(d + " " + o + " " + r + " " + c);

	System.out.print(d + " ");
	
	System.out.print(o + " ");

	System.out.print(r + " ");

	System.out.print(c + " \n");
	
	System.out.printf("%d %d %d %d", d, o, r, c);

	}

}